/**
 * Date Utilities
 * Helper functions for date formatting and display.
 */

/**
 * Formats a date string as a relative time (e.g., "5m ago", "2h ago", "3d ago")
 * Falls back to locale date string for older dates.
 */
export function formatRelativeDate(dateString: string): string {
  const date = new Date(dateString);
  const now = new Date();
  const diffMs = now.getTime() - date.getTime();
  const diffMins = Math.floor(diffMs / 60000);
  const diffHours = Math.floor(diffMs / 3600000);
  const diffDays = Math.floor(diffMs / 86400000);

  if (diffMins < 1) return "Just now";
  if (diffMins < 60) return `${diffMins}m ago`;
  if (diffHours < 24) return `${diffHours}h ago`;
  if (diffDays < 7) return `${diffDays}d ago`;
  return date.toLocaleDateString();
}

/**
 * Formats a date string as a localized time string.
 */
export function formatTime(dateString: string): string {
  return new Date(dateString).toLocaleTimeString();
}
